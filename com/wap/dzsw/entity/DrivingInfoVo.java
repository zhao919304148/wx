package com.wap.dzsw.entity;

public class DrivingInfoVo {
	private String referrerOpenid;// 推荐人
	private String licenseNo;// 车牌号
	private String phoneNo;// 手机号
	private String measureOpendid;// 测算人
	private String chassisNumber;// 车架号
	private String debutDate;// 初登日期
	private String engineNo;// 发动机号
	private String owner;// 车主

	public String getReferrerOpenid() {
		return referrerOpenid;
	}

	public void setReferrerOpenid(String referrerOpenid) {
		this.referrerOpenid = referrerOpenid;
	}

	public String getLicenseNo() {
		return licenseNo;
	}

	public void setLicenseNo(String licenseNo) {
		this.licenseNo = licenseNo;
	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}

	public String getMeasureOpendid() {
		return measureOpendid;
	}

	public void setMeasureOpendid(String measureOpendid) {
		this.measureOpendid = measureOpendid;
	}

	public String getChassisNumber() {
		return chassisNumber;
	}

	public void setChassisNumber(String chassisNumber) {
		this.chassisNumber = chassisNumber;
	}

	public String getDebutDate() {
		return debutDate;
	}

	public void setDebutDate(String debutDate) {
		this.debutDate = debutDate;
	}

	public String getEngineNo() {
		return engineNo;
	}

	public void setEngineNo(String engineNo) {
		this.engineNo = engineNo;
	}

	public String getOwner() {
		return owner;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}

}
